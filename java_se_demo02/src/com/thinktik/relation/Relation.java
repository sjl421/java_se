package com.thinktik.relation;

/**
 * @author think 2018年1月13日下午9:11:14
 * ==,!=,>,>=,<,<= 判断
 */
public class Relation {
	public static void main(String[] args) {
		// ==,!=,>,>=,<,<=
		System.out.println(4 == 3);
		System.out.println(4 != 3);
		System.out.println(4 > 4);
		System.out.println(4 >= 4);
		System.out.println(4 < 3);
		System.out.println(4 <= 3);
	}
}
